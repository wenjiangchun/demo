package com.haze.vsail.bus.entity;

import java.io.Serializable;

public class BusPos implements Serializable {

    private String vin;

    private String x;

    private String y;

    public String getVin() {
        return vin;
    }

    public void setVin(String vin) {
        this.vin = vin;
    }

    public String getX() {
        return x;
    }

    public void setX(String x) {
        this.x = x;
    }

    public String getY() {
        return y;
    }

    public void setY(String y) {
        this.y = y;
    }
}
